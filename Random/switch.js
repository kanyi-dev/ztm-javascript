function moveCommand(direction) {
  let whatHappens;
  switch (direction) {
    case "forward":
      whatHappens = "You encounter a monster";
      break;
    case "forward":
      whatHappens = "You encounter a monster";
      break;
    case "forward":
      whatHappens = "You encounter a monster";
      break;
    case "forward":
      whatHappens = "You encounter a monster";
      break;
    default:
      whatHappens = "Please enter a valid direction";
  }
  return whatHappens;
}
